def glyph_bytes(rows):
    out = []
    for r in rows:
        b = 0
        for i, ch in enumerate(r[:8]):
            if ch != ' ': b |= (0x80 >> i)
        out.append(b)
    return out

lines = ["/* AUTO-GENERATED by tools/make_font.py -- original 8x8 font, ASCII 0x20..0x5F. */",
         "/* 8 bytes per glyph, bit7 = leftmost pixel. Indexed by (c - 0x20). */",
         "const unsigned char mc_font[64][8] = {"]
for code in range(0x20, 0x60):
    ch = chr(code)
    rows = G.get(ch, G[' '])
    lines.append("  { " + ",".join("0x%02x" % b for b in glyph_bytes(rows)) + " }, /* '%s' */" % (ch if ch != ' ' else 'sp'))
lines.append("};")
open(os.path.join(ROOT, "src", "hal", "mc_font.c"), "w").write("\n".join(lines) + "\n")
